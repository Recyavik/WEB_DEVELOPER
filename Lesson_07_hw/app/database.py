# ─── app/database.py ───────────────────────────────────────────────────────
# Этот файл отвечает за подключение к базе данных.
# Здесь настраивается «движок» SQLAlchemy и фабрика сессий.
# Сессия — это объект, через который мы читаем и пишем данные в БД.

import os  # Стандартный модуль Python для работы с операционной системой.
           # Используем os.environ.get() для чтения переменных окружения.

# create_engine — создаёт «движок» (engine): объект, управляющий подключением к БД.
# Engine знает: как подключиться, какой драйвер использовать (psycopg2 для PostgreSQL).
from sqlalchemy import create_engine

# sessionmaker — фабрика сессий: функция, которая создаёт новые сессии по запросу.
# Session — тип данных для аннотаций (используется в main.py).
from sqlalchemy.orm import sessionmaker

# DATABASE_URL — строка подключения к PostgreSQL.
# Формат: postgresql://пользователь:пароль@хост:порт/база_данных
#
# os.environ.get('DATABASE_URL', fallback) — читает переменную окружения DATABASE_URL.
# Если переменная не задана — использует второй аргумент (fallback).
#   - В Docker: DATABASE_URL задана в docker-compose.yml → используется она.
#   - Локально без Docker: переменная не задана → используется localhost.
DATABASE_URL = os.environ.get(
    'DATABASE_URL',                                                       # Имя переменной
    'postgresql://weather_user:weather_pass@localhost:5432/weather_db',   # Значение по умолчанию
)

# create_engine — создаёт движок подключения к БД.
# Движок не открывает соединение сразу — соединение открывается по первому запросу.
# В параметрах можно настроить пул соединений, таймауты и т.д., но нам хватает умолчаний.
engine = create_engine(DATABASE_URL)

# sessionmaker — создаёт класс (фабрику) для генерации сессий.
# Каждый вызов SessionLocal() создаёт новую сессию с настройками ниже.
#
# autocommit=False — не сохранять изменения автоматически после каждой операции.
#   Мы будем явно вызывать db.commit() когда хотим сохранить данные.
#   Это даёт контроль: если что-то пошло не так — можно откатить через db.rollback().
#
# autoflush=False  — не отправлять изменения в БД автоматически перед каждым SELECT.
#   Это даёт предсказуемое поведение: данные уходят в БД только при commit().
#
# bind=engine      — привязываем сессию к нашему движку (нашей БД).
SessionLocal = sessionmaker(autocommit=False, autoflush=False, bind=engine)


def get_db():
    """
    Генератор сессии базы данных.

    FastAPI вызывает эту функцию при каждом HTTP-запросе (через Depends(get_db)).
    Функция создаёт сессию, передаёт её в обработчик маршрута,
    а после ответа гарантированно закрывает её.

    Почему генератор (yield, а не return)?
    Потому что нам нужно выполнить код ПОСЛЕ того, как FastAPI вернёт ответ.
    С return мы не можем это сделать — функция завершается сразу.
    С yield функция «приостанавливается» на yield, FastAPI использует db,
    а потом функция возобновляется и выполняет finally.
    """
    # Создаём новую сессию для этого запроса.
    db = SessionLocal()

    try:
        # yield — передаём сессию в маршрут FastAPI.
        # FastAPI подставит её в параметр db: Session = Depends(get_db).
        # Код ниже yield выполнится ПОСЛЕ того, как маршрут вернёт ответ.
        yield db
    finally:
        # finally — выполняется ВСЕГДА: и при успехе, и при ошибке.
        # db.close() — закрываем соединение и возвращаем его в пул.
        # Без этого соединения накапливались бы и PostgreSQL в итоге отказал бы.
        db.close()
