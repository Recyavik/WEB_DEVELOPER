import psycopg2   # библиотека-«переводчик» между Python и PostgreSQL
                  # без неё Python не умеет разговаривать с базой данных
import os         # модуль для чтения системных переменных окружения
                  # (как словарь настроек, которые хранятся вне кода)


def get_connection():
    """
    Функция открывает соединение с базой данных и возвращает его.
    Аналогия: как позвонить по телефону — сначала набираем номер,
    ждём ответа, и только потом можно разговаривать (читать/писать данные).
    """
    conn = psycopg2.connect(  # psycopg2.connect() — дозваниваемся до БД

        # os.environ.get("ПЕРЕМЕННАЯ", "значение_по_умолчанию")
        # сначала ищет переменную в системе (docker-compose задаёт их),
        # если не нашёл — использует значение по умолчанию (второй аргумент)

        host=os.environ.get("DB_HOST", "localhost"),
        # host — адрес сервера с базой данных
        # "localhost" = «на этом же компьютере»
        # в Docker это будет имя контейнера, например "db"

        dbname=os.environ.get("DB_NAME", "temperatura_db"),
        # dbname — имя конкретной базы данных на сервере
        # (на одном сервере PostgreSQL может быть много разных баз)

        user=os.environ.get("DB_USER", "student"),
        # user — логин пользователя базы данных
        # (не путать с логином Windows/Linux — это отдельный пользователь PostgreSQL)

        password=os.environ.get("DB_PASSWORD", "secret123"),
        # password — пароль этого пользователя

        port=int(os.environ.get("DB_PORT", 5432))
        # port — «номер двери» на сервере (как квартира в доме)
        # 5432 — стандартный порт PostgreSQL по умолчанию
        # int() — преобразуем строку в целое число, потому что порт — это число
    )
    return conn  # возвращаем готовое соединение — наш «открытый телефонный звонок»


def init_db():
    """
    Создаёт таблицу в базе данных при первом запуске.
    Если таблица уже есть — ничего не делает (не сломает данные).
    Аналогия: поставить пустую тетрадь на полку перед первым уроком.
    """
    conn = get_connection()  # открываем соединение с БД (звоним в архив)

    try:  # try/finally — гарантия, что соединение закроется даже при ошибке

        cur = conn.cursor()
        # cursor — это «ручка» для записи в БД
        # через cursor мы отправляем SQL-команды

        cur.execute("""
            CREATE TABLE IF NOT EXISTS temperature_logs (
            -- CREATE TABLE — создать таблицу (как создать новый лист в Excel)
            -- IF NOT EXISTS — только если такой таблицы ещё нет

                id          SERIAL PRIMARY KEY,
                -- id — номер строки (1, 2, 3...)
                -- SERIAL — PostgreSQL сам увеличивает номер при каждой новой записи
                -- PRIMARY KEY — главный ключ, каждый id уникален

                server_name VARCHAR(50)  NOT NULL,
                -- server_name — имя сервера (текст)
                -- VARCHAR(50) — текст длиной до 50 символов
                -- NOT NULL — обязательное поле, пустым быть не может

                temperature NUMERIC(5,2) NOT NULL,
                -- temperature — температура
                -- NUMERIC(5,2) — число с 5 цифрами всего и 2 после запятой
                --   например: 123.45 — допустимо, 1234.56 — тоже (всего 6 цифр — нет, ограничение 5)
                -- NOT NULL — обязательное поле

                status      VARCHAR(20)  NOT NULL,
                -- status — статус: 'Норма', 'Предупреждение', 'Критично'
                -- VARCHAR(20) — текст до 20 символов

                recorded_at TIMESTAMPTZ DEFAULT NOW()
                -- recorded_at — дата и время записи
                -- TIMESTAMPTZ — дата+время с учётом часового пояса (Z = timezone)
                -- DEFAULT NOW() — если не указать время явно, ставится текущий момент
            )
        """)
        # execute() — отправляем SQL-запрос в базу данных

        conn.commit()
        # commit() — «подписываем документ», сохраняем изменения навсегда
        # без commit() база данных не сохранит наши изменения!

    finally:
        conn.close()
        # close() — закрываем соединение (кладём трубку телефона)
        # finally: — этот блок выполнится ВСЕГДА, даже если была ошибка выше
        # это важно — незакрытые соединения «засоряют» сервер БД


def determine_status(temperature: float) -> str:
    """
    Определяет текстовый статус по числовому значению температуры.
    temperature: float — принимает число с плавающей точкой (дробное)
    -> str — возвращает строку ('Норма', 'Предупреждение' или 'Критично')
    """
    if temperature < 60:        # если температура меньше 60 градусов
        return "Норма"          # всё хорошо, возвращаем "Норма"
    elif temperature < 80:      # иначе если меньше 80 (значит от 60 до 79.99)
        return "Предупреждение" # возвращаем предупреждение
    else:                       # во всех остальных случаях (80 и выше)
        return "Критично"       # возвращаем критичный статус


def insert_measurement(server_name: str, temperature: float, status: str):
    """
    Сохраняет одно измерение температуры в базу данных.
    server_name: str — имя сервера (строка)
    temperature: float — температура (дробное число)
    status: str — статус ('Норма' / 'Предупреждение' / 'Критично')
    Аналогия: вписать новую строчку в журнал наблюдений.
    """
    conn = get_connection()  # открываем соединение с БД

    try:
        cur = conn.cursor()  # создаём курсор (берём «ручку» для записи)

        cur.execute(
            # SQL-запрос INSERT — добавить новую строку в таблицу
            # %s — это «заглушки» (плейсхолдеры), реальные значения придут ниже
            # ВАЖНО: никогда не вставляй данные напрямую в строку SQL!
            # Это защита от SQL-инъекций — атаки, когда злоумышленник
            # вводит SQL-код в форму вместо обычного текста
            """
            INSERT INTO temperature_logs (server_name, temperature, status)
            VALUES (%s, %s, %s)
            """,
            (server_name, temperature, status)
            # второй аргумент — кортеж с реальными данными
            # psycopg2 сам безопасно подставит их вместо %s
            # порядок важен: первый %s = server_name, второй = temperature, третий = status
        )

        conn.commit()  # сохраняем транзакцию — без этого запись не появится в БД!

    finally:
        conn.close()  # всегда закрываем соединение


def get_history(limit: int = 10) -> list[tuple]:
    """
    Получает последние измерения из базы данных.
    limit: int = 10 — сколько записей вернуть (по умолчанию 10)
    -> list[tuple] — возвращает список кортежей (строк таблицы)
    Аналогия: попросить архивариуса показать последние N записей журнала.
    """
    conn = get_connection()  # открываем соединение с БД

    try:
        cur = conn.cursor()  # создаём курсор

        cur.execute(
            # SQL-запрос SELECT — прочитать данные из таблицы
            # SELECT поля FROM таблица — выбираем только нужные колонки
            """
            SELECT server_name, temperature, status, recorded_at
            FROM temperature_logs
            ORDER BY recorded_at DESC
            -- ORDER BY recorded_at DESC — сортируем по дате, DESC = по убыванию
            -- (самые новые записи — первые)
            LIMIT %s
            -- LIMIT — ограничиваем количество строк в ответе
            """,
            (limit,)
            # передаём limit как кортеж — обязательна запятая после значения!
            # (10,) — это кортеж из одного элемента
            # (10)  — это просто число в скобках, НЕ кортеж
        )

        rows = cur.fetchall()
        # fetchall() — забираем ВСЕ строки результата
        # каждая строка — это кортеж: ('srv-01', 65.5, 'Предупреждение', datetime(...))
        # rows будет выглядеть как:
        # [('srv-01', 65.5, 'Предупреждение', ...), ('db-node', 45.0, 'Норма', ...)]

        return rows  # возвращаем список кортежей в вызывающую функцию

    finally:
        conn.close()  # закрываем соединение в любом случае
