# create_engine — создаёт «движок» (engine), который умеет разговаривать с базой данных
from sqlalchemy import create_engine

# sessionmaker — фабрика для создания сессий (временных соединений с базой)
from sqlalchemy.orm import sessionmaker

# os — модуль для работы с операционной системой (читаем переменные окружения)
import os

# Читаем строку подключения из переменной окружения DATABASE_URL
# Если переменная не задана — используем значение по умолчанию (для запуска через docker-compose)
# Формат строки подключения: postgresql://пользователь:пароль@хост:порт/имя_базы
DATABASE_URL = os.getenv(
    "DATABASE_URL",                                       # имя переменной окружения, которую ищем
    "postgresql://postgres:postgres@db:5432/robots_db"   # значение по умолчанию, если переменная не найдена
)

# Создаём движок — один на всё приложение
# Движок управляет пулом соединений (несколько запросов могут работать параллельно)
# DATABASE_URL — строка подключения, которую мы прочитали выше
engine = create_engine(DATABASE_URL)

# Создаём фабрику сессий — SessionLocal это «шаблон», по которому будут создаваться сессии
# Каждый HTTP-запрос получит свою отдельную, изолированную сессию
SessionLocal = sessionmaker(
    bind=engine,       # привязываем сессии к нашему движку (то есть к нашей базе данных)
    autocommit=False,  # НЕ сохранять изменения автоматически — мы сами вызовем db.commit()
    autoflush=False    # НЕ отправлять SQL-запросы в базу автоматически — только при commit()
)


# Генератор-зависимость для FastAPI (Dependency Injection)
# FastAPI вызывает эту функцию перед каждым запросом и передаёт сессию в роут
def get_db():
    # Создаём новую сессию для этого конкретного HTTP-запроса
    db = SessionLocal()
    try:
        # yield — «отдаём» сессию в роут-функцию и ждём, пока она закончит работу
        # Всё, что после yield, выполнится после завершения роута
        yield db
    finally:
        # Закрываем сессию в любом случае — даже если в роуте произошла ошибка
        # finally гарантирует выполнение этого кода всегда
        db.close()
