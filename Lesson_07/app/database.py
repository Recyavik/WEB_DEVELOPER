"""
app/database.py — настройка подключения к PostgreSQL через SQLAlchemy.

Здесь создаётся движок (engine) и фабрика сессий (SessionLocal).
Каждый HTTP-запрос получает свою сессию через функцию get_db().
"""

import os                                       # Для чтения переменных окружения
from sqlalchemy import create_engine            # Создаём движок подключения к БД
from sqlalchemy.orm import sessionmaker         # Фабрика сессий

# ─── URL подключения к базе данных ────────────────────────────────────────────
# Читаем из переменной окружения DATABASE_URL (задаётся в docker-compose.yml)
# Если переменная не задана — используем значение по умолчанию для локальной разработки
DATABASE_URL = os.environ.get(
    'DATABASE_URL',                             # Имя переменной окружения
    'postgresql://mks_user:mks_pass@db:5432/mks_db'  # Значение по умолчанию
)

# ─── Движок SQLAlchemy ────────────────────────────────────────────────────────
# engine — это основной объект для работы с PostgreSQL.
# Аналогия: это как телефонная линия между нашим кодом и базой данных.
engine = create_engine(DATABASE_URL)            # Создаём подключение к PostgreSQL

# ─── Фабрика сессий ───────────────────────────────────────────────────────────
# SessionLocal — это «завод» по производству сессий.
# Каждый запрос получает свою отдельную сессию через get_db().
SessionLocal = sessionmaker(
    autocommit=False,                           # Не сохранять изменения автоматически
    autoflush=False,                            # Не синхронизировать с БД автоматически
    bind=engine                                 # Привязываем к нашему движку
)


def get_db():
    """
    Генератор сессии базы данных.

    Используется как зависимость (Depends) в роутах FastAPI.
    Автоматически закрывает сессию после завершения запроса —
    даже если произошла ошибка.

    Аналогия: как аренда рабочего места. Берёшь в начале запроса,
    возвращаешь в конце — независимо от того, всё ли прошло хорошо.
    """
    db = SessionLocal()                         # Открываем новую сессию
    try:
        yield db                                # Отдаём сессию в роут (через Depends)
    finally:
        db.close()                              # Закрываем сессию после завершения запроса
